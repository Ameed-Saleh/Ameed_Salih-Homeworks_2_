'''
Part A
For each question: What is the missing operation (?)

Example:
{1, 2} ? {3, 4} = {1, 2, 3, 4}
Answer: |
'''

# 1 --> {1, 2} ? {3, 4} = {1, 2, 3, 4}
print(f"RESULT 1  :{({1, 2} | {3, 4})}") # |

# 2 --> {1, 2, 3} ? {3, 4} = {3}
print(f"RESULT 2 :{({1, 2, 3} & {3, 4})}") # &

# 3 --> {1, 2, 3, 4} ? {3, 4} = {1, 2}
print(f"RESULT 3 :{({1, 2, 3, 4} - {3, 4})}") # -

# 4 --> {1, 2, 3} ? {3, 4} = {1, 2, 4}
print(f"RESULT 4 :{({1, 2, 3} ^ {3, 4})}") # ^

# 5 --> {1, 2, 3, 4} ? {1, 2} = True
print(f"RESULT 5 :{({1, 2, 3, 4} >= {1, 2})}") # >=

# 6 --> {1, 2} ? {1, 2, 3, 4} = True
print(f"RESULT 6 :{({1, 2} <= {1, 2, 3, 4})}") # <=

# 7 --> {5, 6} ? {6, 7} = {5, 6, 7}
print(f"RESULT 7 :{({5, 6} | {6, 7})}") # |

# 8 --> {10, 11, 12} ? {12, 13} = {10, 11}
print(f"RESULT 8 :{({10, 11, 12} - {12, 13})}") # -