'''
Part B - Bonus
For each question: What are the two missing operations (? ?)

Example:

{1, 2} ? {3, 4} ? {2} = {1, 3, 4}

Answer: |  - , because-

{1, 2} | {3, 4} - {2} = {1, 3, 4}

You can also add brackets
Example:

{1 2 3} ? {3 4} ? { 2 } = { 2 }

Answer:

( {1 2 3} - {3 4} ) & { 2 } = { 2 }

Solve:
'''

# 1 --> {1, 2} ? {3, 4} ? {2} = {1, 3, 4}
print(f"RESULT 1 :{({1, 2} | {3, 4}) - {2}}")                    # | , -

# 2 --> {1, 2, 3, 4} ? {3, 4} ? {1, 2} = {1, 2, 3, 4}
print(f"RESULT 2 :{({1, 2, 3, 4} | {3, 4} ^ {1,2})}")            # | , ^

# 3 --> {1, 2, 3} ? {2, 3} ? {1, 4} = {2, 3, 4}
print(f"RESULT 3 :{(({1, 2, 3} | {2, 3}) ^ {1,4})}")             # | , ^

# 4 --> {1, 2, 3, 4, 5} ? {2, 4} ? {1, 2, 3} = {4, 5}
print(f"RESULT 4 :{(({1, 2, 3, 4, 5} | {2, 4} ) - {1, 2, 3})}")  # | , -

# 5 --> {1, 2, 3, 4} ? {2, 4, 6} ? {2} = {2}
print(f"RESULT 5 :{({1, 2, 3, 4} & {2, 4, 6} & {2})}")           # & , &

# 6 --> {1, 2, 3, 4} ? {3, 4, 5} ? {1, 5} = {2, 3, 4}
print(f"RESULT 6 :{(({1, 2, 3, 4} | {3, 4, 5} ) - {1, 5})}")     # | , -

# 7 --> {1, 2, 3} ? {3, 4, 5} ? {1, 2, 4} = {4, 5}
print(f"RESULT 7 :{({1, 2, 3} ^ ({3, 4, 5}  | {1, 2, 4}))}")     # ^ , |
